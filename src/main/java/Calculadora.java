public class Calculadora {

    //Método somar espera dois valores inteiros e retorna um número inteiro referente a soma dos
    // valores passados como argumento
    public int soma(int a, int b) {
        return a + b;
    }

    //Método subtracao espera dois valores inteiros e retorna um número inteiro referente a subtracao dos
    // valores passados como argumento
    public int subtracao(int a, int b) {
        return a - b;
    }

    //Método multiplicacao espera dois valores inteiros e retorna um número inteiro referente a multiplicacao
    // dos valores passados como argumento
    public int multiplicacao(int a, int b) {
        return a * b;
    }

    //Método divisao espera dois valores inteiros e retorna um número inteiro referente a divisao dos
    // valores passados como argumento
    public int divisao(int a, int b) {
        return a / b;
    }

}
